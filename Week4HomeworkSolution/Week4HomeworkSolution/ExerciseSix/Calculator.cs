namespace Week4HomeworkSolution.ExerciseSix
{
    public class Calculator
    {
        // Method for adding two integers
        public int Add(int x, int y)
        {
            return x + y;
        }

        // Method for subtracting two integers
        public int Subtract(int x, int y)
        {
            return x - y;
        }

        // Method for multiplying two integers
        public int Multiply(int x, int y)
        {
            return x * y;
        }

        // Method for dividing two integers
        public int Divide(int x, int y)
        {
            return x / y;
        }

        // Method for raising a double to a power
        public double Power(double x, double y)
        {
            return Math.Pow(x, y);
        }

        // Method for finding the square root of a double
        public double SquareRoot(double x)
        {
            return Math.Sqrt(x);
        }
    }
}
